    private void jPanel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MouseClicked
           String contrasena = new String(txtCntrasena.getPassword()); 
        Administrativo administrativo  = new Administrativo(txtCargo.getText(), txtNombre.getText(), txtApellido.getText(), txtCorreo.getText(), contrasena, Integer.parseInt(txtDocumentoIdentidad.getText()));
        
        if (daosRegistroAdmi.agregar(administrativo)) {
            JOptionPane.showMessageDialog(jPanel1, " Administrativo registrado con exito!", "EXITO!!", JOptionPane.INFORMATION_MESSAGE);
            limpiarCajas();
            
        } else {
              JOptionPane.showMessageDialog(jPanel1, " Intenta nuevamnete!", "ERROR!!", JOptionPane.INFORMATION_MESSAGE);
            
        }
    }//GEN-LAST:event_jPanel2MouseClicked

